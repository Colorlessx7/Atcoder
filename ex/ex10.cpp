#include <bits/stdc++.h>
using namespace std;

int main() {
  int A, B;
  cin >> A >> B;
  int i = 0;
  // ここにプログラムを追記
  cout << "A:";
  while (i < A)
  {
      cout << "]";
      i++; 
  }
  cout << endl;
  cout << "B:";
  i = 0;
  while (i < B)
  {
      cout << "]";
      i++;
  }
  cout << endl;
}
